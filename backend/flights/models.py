from django.db import models

class Flight(models.Model):
  year = models.IntegerField()
  month = models.IntegerField()
  day_of_week = models.IntegerField()
  date = models.DateField()
  carrier = models.CharField(max_length=255)
  airline_id = models.IntegerField()
  tail_num = models.CharField(max_length=255)
  flight_num = models.IntegerField()
  origin_airport_id = models.CharField(max_length=255)
  origin = models.CharField(max_length=255)
  origin_city_name = models.CharField(max_length=255)
  origin_state_abr = models.CharField(max_length=255)
  origin_state_fips = models.CharField(max_length=255)
  origin_state_name = models.CharField(max_length=255)
  dest_airport_id = models.CharField(max_length=255)
  dest_city_name = models.CharField(max_length=255)
  dest_state_abr = models.CharField(max_length=255)
  dest_state_fips = models.CharField(max_length=255)
  dest_state_name = models.CharField(max_length=255)
  crs_dep_time = models.CharField(max_length=255)
  dep_time = models.CharField(max_length=255)
  dep_delay = models.IntegerField()
  dep_delay_new = models.IntegerField()
  dep_delay_fifteen = models.IntegerField()
  dep_delay_group = models.IntegerField()
  dep_time_blk = models.CharField(max_length=255)
  taxi_out = models.IntegerField()
  wheels_off = models.CharField(max_length=255)
  wheels_on = models.CharField(max_length=255)
  taxi_in = models.IntegerField()
  crs_arr_time = models.CharField(max_length=255)
  arr_time = models.CharField(max_length=255)
  arr_delay = models.IntegerField()
  arr_delay_new = models.IntegerField()
  arr_delay_fifteen = models.IntegerField()
  arr_delay_group = models.IntegerField()
  arr_time_blk = models.CharField(max_length=255)
  cancelled = models.IntegerField()
  cancellation_code = models.CharField(max_length=255)
  diverted = models.IntegerField()
  crs_elapsed_time = models.IntegerField()
  actual_elapsed_time = models.IntegerField()
  air_time = models.IntegerField()
  carrier_delay = models.IntegerField()
  nas_delay = models.IntegerField()
  security_delay = models.IntegerField()
  late_aircraft_delay = models.IntegerField()
  